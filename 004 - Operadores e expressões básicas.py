num1 = 10       
num2 = 3
num3 = 6

print("\n---Operações Aritméticas---")
print("Adição (10+3+6)", num1+num2+num3)
print("Subtração (10-3-6)", num1-num2-num3)
print("Multiplicação (10*3*6)", num1*num2*num3)
print("Divisão (10/3)", num1/num2)
print("Divisão Floor (10//6)", num1//num3)
print("Modulus (10%6)", num1 % num3)
print("Exponenciação (10**6)", num1 ** num3)

print("\n---Operações Relacionais---")
print("10 é igual a 3", num1 == num2)
print("10 não é igual a 3", num1 != num2)
print("10 é maior que 6", num1 > num3)
print("10 é menor que 6", num1 < num3)
print("10 é maior ou igual a 6", num1 >= num3)
print("10 é menor ou igual a 6", num1 <= num3)

true_valor = True
false_valor = False

print("\n---Operações Lógicas---")
print("True and false:", true_valor and false_valor)
print("True and true:", true_valor and true_valor)
print("False and false:", false_valor and false_valor)
print("True or false:", true_valor or false_valor)
print("True or true:", true_valor or true_valor)
print("False or false:", false_valor or false_valor)
print("Not true", not true_valor)
print("Not false", not false_valor)
